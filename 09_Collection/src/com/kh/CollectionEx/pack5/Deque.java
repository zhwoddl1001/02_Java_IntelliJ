package com.kh.CollectionEx.pack5;

public class Deque {
    /*
    * Queue : FIFO(First In First Out) 방식으로 데이터를 처리하는 자료구조
    *         예 줄서기 (먼저 줄선 사람이 먼저 나감)
    * 
    * 데이터 추가 : offer() 또는 add()
    * 데이터 제거 : poll() 또는 remove()
    * 데이터 확인 : peek() 또는 element()
    * 
    * 
    * Deque (Double Ended Queue)
    * 양방향 큐로 값을 양쪽 끝에서 추가하거나 제거할 수 있음
    * 원한다면 LIFO(Last IN - First Out) 형식으로 활용하거나
    *        FIFO(First In First Out) 방식으로 활용할 수 있음
    *
    *  LIFO(Last IN - First Out)
    *         예) 지하철에 꽊 찼을 때 맨 마지막에 탄사람이 제일 먼저 내림
     * */
}
